# ── Puppeteer PDF rendering ───────────────────────────────────────────────

def _render_pdf(html_content: str, property_id: str, report_month: str) -> Optional[bytes]:
    """
    Render HTML to PDF via Puppeteer (Node.js subprocess).
    Writes HTML to a temp file, calls puppeteer_renderer.js,
    reads the PDF output and returns bytes.
    """
    if not PUPPETEER_SCRIPT.exists():
        logger.error(f"[TS-23] Puppeteer renderer not found at {PUPPETEER_SCRIPT}")
        return None

    try:
        with tempfile.NamedTemporaryFile(
            suffix=".html", mode="w", encoding="utf-8", delete=False
        ) as html_file:
            html_file.write(html_content)
            html_path = html_file.name

        with tempfile.NamedTemporaryFile(suffix=".pdf", delete=False) as pdf_file:
            pdf_path = pdf_file.name

        result = subprocess.run(
            ["node", str(PUPPETEER_SCRIPT), html_path, pdf_path],
            capture_output=True,
            text=True,
            timeout=60,
        )

        if result.returncode != 0:
            logger.error(f"[TS-23] Puppeteer failed: {result.stderr[:300]}")
            return None

        with open(pdf_path, "rb") as f:
            return f.read()

    except subprocess.TimeoutExpired:
        logger.error(f"[TS-23] Puppeteer timed out for property {property_id}")
        return None
    except Exception as exc:
        logger.error(f"[TS-23] PDF render error: {exc}")
        return None
    finally:
        import os as _os
        for path in (html_path, pdf_path):
            try:
                _os.unlink(path)
            except Exception:
                pass


def _upload_pdf_to_r2(
    pdf_bytes: bytes,
    client_id: str,
    property_id: str,
    report_month: str,
) -> Optional[str]:
    """Upload the rendered PDF to Cloudflare R2."""
    try:
        from agents.agent3.r2_storage import _get_r2_client, R2_PUBLIC_BASE
        key = f"reports/{client_id}/{property_id}/{report_month}.pdf"
        bucket = "staylio-reports"
        client = _get_r2_client()
        client.put_object(
            Bucket=bucket,
            Key=key,
            Body=pdf_bytes,
            ContentType="application/pdf",
            CacheControl="private, no-cache",
        )
        return f"{R2_PUBLIC_BASE}/reports/{key}"
    except Exception as exc:
        logger.error(f"[TS-23] R2 PDF upload failed: {exc}")
        return None
